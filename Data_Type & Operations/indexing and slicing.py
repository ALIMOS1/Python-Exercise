# indexing and slicing a string

mystring = 'Hello World'

print(mystring[-3])  # indexing


my2string = 'Hello World'

print(my2string[:3]) # from the beggining up to the end


print(my2string[3:]) # from the 3 to th end

print(my2string[6:11]) # from 6 index to the 10th index (subsections)

print(my2string[::2]) # step size go from the begging till the end with 2 steps size

print(my2string[::-1]) # revers a string

print(my2string[:5:-1]) # reverse a string up until 4th index position




